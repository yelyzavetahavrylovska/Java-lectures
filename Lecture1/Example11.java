package Module1.lesson1;

public class Example11 {
    public static void main(String[] args) {
        int a = 0;
        int b = 0;
        int c = 0;

        System.out.println(a--);
        System.out.println(a);
        System.out.println("======");

        System.out.println(--b);
        System.out.println(b);
        System.out.println("=======");

        System.out.println(c++);
        System.out.println(c);
        System.out.println("=======");

        System.out.println(++c);
        System.out.println(c);
        System.out.println("========");

        int d = 0;
        d = d + 1;
        System.out.println(d);



    }
}
